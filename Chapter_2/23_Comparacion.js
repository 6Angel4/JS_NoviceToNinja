/**
 * Tipos de comparacion
 */

// Igualdad Suave
5 == 5; //true
5 == "5"; //true (coercion de tipos)
// Igualdad Estricta
5 === 5; // true, porque tienen el mismo valor y son del mismo tipo de dato
5 === "5"; // false, porque aunque sean el mismo valor, no son del mismo tipo
// Desigualdad
16 != "16"; //seria true, pero como se esta negando, devuelve false
16 !== "16"; //true, porque no son iguales de tipo
// Mayor que
8 > 4;
// Menor que
8 < 4;
// Mayor o igual que
8 >= 4;
8 >= 8;
// Menor o igual que
8 <= 4;
