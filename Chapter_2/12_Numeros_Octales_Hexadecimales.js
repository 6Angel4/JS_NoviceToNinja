/**
 * Si un numero inicia con 0x, es considerado hexadecimal (base 16)
 * Los numeros Hexadecimales o hex son utilizazdos a menudo para colores en codigo web
 * Si un numero inicia con 0o, es considerado octal (base 8)
 * Si un numero inicia con 0b, es considerado binario (base 2)
 */
console.log(0xaf); //    a representa 10, f representa 15
console.log(0o47); //    4 ochos y 7 unidades ((4*8)+7)
console.log(0b1010); //  1 ocho, 0 cuatros, 1 dos, 0 ceros (8+0+2+0)
